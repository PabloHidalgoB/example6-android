public class CurrencyPDB {

    private String mCode;
    private String mAlphaCode;
    private String mNumericCode;
    private String mName;
    private Double mRate;
    private String mDate;

    private int mImagen;
    private String mMoneda;
    private String mValor;


    public String getmMoneda() {
        return mMoneda;
    }

    public void setmMoneda(String mMoneda) {
        this.mMoneda = mMoneda;
    }

    public String getmValor() {
        return mValor;
    }

    public void setmValor(String mValor) {
        this.mValor = mValor;
    }

    public int getmImagen() {
        return mImagen;
    }

    public void setmImagen(int mImagen) {
        this.mImagen = mImagen;
    }

    public String getmCode() {
        return mCode;
    }

    public void setmCode(String mCode) {
        this.mCode = mCode;
    }

    public String getmAlphaCode() {
        return mAlphaCode;
    }

    public void setmAlphaCode(String mAlphaCode) {
        this.mAlphaCode = mAlphaCode;
    }

    public String getmNumericCode() {
        return mNumericCode;
    }

    public void setmNumericCode(String mNumericCode) {
        this.mNumericCode = mNumericCode;
    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public Double getmRate() {
        return mRate;
    }

    public void setmRate(Double mRate) {
        this.mRate = mRate;
    }

    public String getmDate() {
        return mDate;
    }

    public void setmDate(String mDate) {
        this.mDate = mDate;
    }
}
